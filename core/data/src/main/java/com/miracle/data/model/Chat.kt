package com.miracle.data.model

import org.drinkless.tdlib.TdApi

data class Chat(
    val id: Long = 0L,
    val type: TdApi.ChatType = TdApi.ChatTypeBasicGroup(),
    val title: String = "",
    val photo: TdApi.ChatPhotoInfo? = null,
    val accentColorId: Int = 0,
    val backgroundCustomEmojiId: Long = 0L,
    val profileAccentColorId: Int = 0,
    val profileBackgroundCustomEmojiId: Long = 0L,
    val permissions: TdApi.ChatPermissions = TdApi.ChatPermissions(),
    val lastMessage: TdApi.Message? = null,
    val positions: List<TdApi.ChatPosition> = emptyList(),
    val chatLists: List<TdApi.ChatList> = emptyList(),
    val messageSenderId: TdApi.MessageSender? = null,
    val blockList: TdApi.BlockList? = null,
    val hasProtectedContent: Boolean = false,
    val isTranslatable: Boolean = false,
    val isMarkedAsUnread: Boolean = false,
    val viewAsTopics: Boolean = false,
    val hasScheduledMessages: Boolean = false,
    val canBeDeletedOnlyForSelf: Boolean = false,
    val canBeDeletedForAllUsers: Boolean = false,
    val canBeReported: Boolean = false,
    val defaultDisableNotification: Boolean = false,
    val unreadCount: Int = 0,
    val lastReadInboxMessageId: Long = 0L,
    val lastReadOutboxMessageId: Long = 0L,
    val unreadMentionCount: Int = 0,
    val unreadReactionCount: Int = 0,
    val notificationSettings: TdApi.ChatNotificationSettings = TdApi.ChatNotificationSettings(),
    val availableReactions: TdApi.ChatAvailableReactions = TdApi.ChatAvailableReactionsAll(),
    val messageAutoDeleteTime: Int = 0,
    val emojiStatus: TdApi.EmojiStatus? = null,
    val background: TdApi.ChatBackground? = null,
    val themeName: String = "",
    val actionBar: TdApi.ChatActionBar? = null,
    val businessBotManageBar: TdApi.BusinessBotManageBar? = null,
    val videoChat: TdApi.VideoChat = TdApi.VideoChat(),
    val pendingJoinRequests: TdApi.ChatJoinRequestsInfo? = null,
    val replyMarkupMessageId: Long = 0L,
    val draftMessage: TdApi.DraftMessage? = null,
    val clientData: String = ""
)

fun TdApi.Chat.toChat() = Chat(
    id = id,
    type = type,
    title = title,
    photo = photo,
    accentColorId = accentColorId,
    backgroundCustomEmojiId = backgroundCustomEmojiId,
    profileAccentColorId = profileAccentColorId,
    profileBackgroundCustomEmojiId = profileBackgroundCustomEmojiId,
    permissions = permissions,
    lastMessage = lastMessage,
    positions = positions.toList(),
    chatLists = chatLists.toList(),
    messageSenderId = messageSenderId,
    blockList = blockList,
    hasProtectedContent = hasProtectedContent,
    isTranslatable = isTranslatable,
    isMarkedAsUnread = isMarkedAsUnread,
    viewAsTopics = viewAsTopics,
    hasScheduledMessages = hasScheduledMessages,
    canBeDeletedOnlyForSelf = canBeDeletedOnlyForSelf,
    canBeDeletedForAllUsers = canBeDeletedForAllUsers,
    canBeReported = canBeReported,
    defaultDisableNotification = defaultDisableNotification,
    unreadCount = unreadCount,
    lastReadInboxMessageId = lastReadInboxMessageId,
    lastReadOutboxMessageId = lastReadOutboxMessageId,
    unreadMentionCount = unreadMentionCount,
    unreadReactionCount = unreadReactionCount,
    notificationSettings = notificationSettings,
    availableReactions = availableReactions,
    messageAutoDeleteTime = messageAutoDeleteTime,
    emojiStatus = emojiStatus,
    background = background,
    themeName = themeName,
    actionBar = actionBar,
    businessBotManageBar = businessBotManageBar,
    videoChat = videoChat,
    pendingJoinRequests = pendingJoinRequests,
    replyMarkupMessageId = replyMarkupMessageId,
    draftMessage = draftMessage,
    clientData = clientData
)
