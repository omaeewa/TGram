package com.miracle.data.model

import org.drinkless.tdlib.TdApi

data class Chat(
    val id: Long,
    val type: TdApi.ChatType,
    val title: String,
    val photo: TdApi.ChatPhotoInfo?,
    val accentColorId: Int,
    val backgroundCustomEmojiId: Long,
    val profileAccentColorId: Int,
    val profileBackgroundCustomEmojiId: Long,
    val permissions: TdApi.ChatPermissions,
    val lastMessage: TdApi.Message?,
    val positions: List<TdApi.ChatPosition>,
    val chatLists: List<TdApi.ChatList>,
    val messageSenderId: TdApi.MessageSender?,
    val blockList: TdApi.BlockList?,
    val hasProtectedContent: Boolean,
    val isTranslatable: Boolean,
    val isMarkedAsUnread: Boolean,
    val viewAsTopics: Boolean,
    val hasScheduledMessages: Boolean,
    val canBeDeletedOnlyForSelf: Boolean,
    val canBeDeletedForAllUsers: Boolean,
    val canBeReported: Boolean,
    val defaultDisableNotification: Boolean,
    val unreadCount: Int,
    val lastReadInboxMessageId: Long,
    val lastReadOutboxMessageId: Long,
    val unreadMentionCount: Int,
    val unreadReactionCount: Int,
    val notificationSettings: TdApi.ChatNotificationSettings,
    val availableReactions: TdApi.ChatAvailableReactions,
    val messageAutoDeleteTime: Int,
    val emojiStatus: TdApi.EmojiStatus?,
    val background: TdApi.ChatBackground?,
    val themeName: String,
    val actionBar: TdApi.ChatActionBar?,
    val businessBotManageBar: TdApi.BusinessBotManageBar?,
    val videoChat: TdApi.VideoChat,
    val pendingJoinRequests: TdApi.ChatJoinRequestsInfo?,
    val replyMarkupMessageId: Long,
    val draftMessage: TdApi.DraftMessage?,
    val clientData: String
)

fun TdApi.Chat.toChat() = Chat(
    id = id,
    type = type,
    title = title,
    photo = photo,
    accentColorId = accentColorId,
    backgroundCustomEmojiId = backgroundCustomEmojiId,
    profileAccentColorId = profileAccentColorId,
    profileBackgroundCustomEmojiId = profileBackgroundCustomEmojiId,
    permissions = permissions,
    lastMessage = lastMessage,
    positions = positions.toList(),
    chatLists = chatLists.toList(),
    messageSenderId = messageSenderId,
    blockList = blockList,
    hasProtectedContent = hasProtectedContent,
    isTranslatable = isTranslatable,
    isMarkedAsUnread = isMarkedAsUnread,
    viewAsTopics = viewAsTopics,
    hasScheduledMessages = hasScheduledMessages,
    canBeDeletedOnlyForSelf = canBeDeletedOnlyForSelf,
    canBeDeletedForAllUsers = canBeDeletedForAllUsers,
    canBeReported = canBeReported,
    defaultDisableNotification = defaultDisableNotification,
    unreadCount = unreadCount,
    lastReadInboxMessageId = lastReadInboxMessageId,
    lastReadOutboxMessageId = lastReadOutboxMessageId,
    unreadMentionCount = unreadMentionCount,
    unreadReactionCount = unreadReactionCount,
    notificationSettings = notificationSettings,
    availableReactions = availableReactions,
    messageAutoDeleteTime = messageAutoDeleteTime,
    emojiStatus = emojiStatus,
    background = background,
    themeName = themeName,
    actionBar = actionBar,
    businessBotManageBar = businessBotManageBar,
    videoChat = videoChat,
    pendingJoinRequests = pendingJoinRequests,
    replyMarkupMessageId = replyMarkupMessageId,
    draftMessage = draftMessage,
    clientData = clientData
)
